struct BRTabBarStyleConfig {
    /// the tincolor for icon
    UIColor *iconTintcolor;
    /// the textcolor for title
    UIColor *textColor;
    /// the font for title
    UIFont *titleFont;
    /// the size for image
    CGSize imageSize;
    /// the topspace for icon
    CGFloat topSpace;
    /// the selected imagecolor;
    UIColor *selectedIconTintColor;
    /// the selected titlecolor;
    UIColor *selectedTitleColor;
};

typedef struct BRTabBarStyleConfig  BRTabBarStyleConfig;


